// ── Bot lifecycle ──
function startBot() {
  if (botProcess) return;

  const config = loadConfig();
  writeEnvFile(config);

  const botScript = app.isPackaged
    ? path.join(process.resourcesPath, 'bot', 'dist', 'bot.cjs')
    : path.join(__dirname, '..', 'dist', 'bot.cjs');

  if (!fs.existsSync(botScript)) {
    sendToRenderer('bot:error', 'Bot not built. Run "npm run build:bot" first.');
    return;
  }

  botLogs = [];
  sendToRenderer('bot:status', 'starting');

  botProcess = fork(botScript, [], {
    cwd: app.isPackaged ? path.join(process.resourcesPath, 'bot') : path.join(__dirname, '..'),
    env: { ...process.env, ...buildBotEnv(config) },
    stdio: ['pipe', 'pipe', 'pipe', 'ipc'],
  });

  botProcess.stdout.on('data', (data) => {
    const line = data.toString().trim();
    if (line) addLog('info', line);
  });

  botProcess.stderr.on('data', (data) => {
    const line = data.toString().trim();
    if (line) addLog('error', line);
  });

  botProcess.on('message', (msg) => {
    sendToRenderer('bot:message', msg);
  });

  botProcess.on('exit', (code) => {
    addLog('info', `Bot process exited with code ${code}`);
    botProcess = null;
    sendToRenderer('bot:status', 'stopped');
  });

  botProcess.on('error', (err) => {
    addLog('error', `Bot error: ${err.message}`);
    botProcess = null;
    sendToRenderer('bot:status', 'error');
  });

  sendToRenderer('bot:status', 'running');
}

function stopBot() {
  if (!botProcess) return;
  botProcess.kill('SIGTERM');
  setTimeout(() => {
    if (botProcess) {
      botProcess.kill('SIGKILL');
      botProcess = null;
    }
  }, 5000);
  sendToRenderer('bot:status', 'stopping');
}

function dotenvParse(config) {
  const env = {};
  for (const [key, value] of Object.entries(config)) {
    if (typeof value === 'string' || typeof value === 'number' || typeof value === 'boolean') {
      env[key] = String(value);
    }
  }
  return env;
}

